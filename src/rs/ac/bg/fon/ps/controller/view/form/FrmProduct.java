    private void prepareView(FormMode formMode) {
        fillCbMeasurementUnit();
        fillCbManufacturer();
        setupComponents(formMode);
    }

    private void fillCbMeasurementUnit() {
        cbMeasurementUnit.removeAllItems();
        for(MeasurementUnit unit : MeasurementUnit.values()) {
            cbMeasurementUnit.addItem(unit);
        }
    }

    private void fillCbManufacturer() {
        cbManufacturer.removeAllItems();
        List<Manufacturer> manufacturers = Controller.getInstance().getAllManufacturers();
        for (Manufacturer manufacturer : manufacturers) {
            cbManufacturer.addItem(manufacturer);
        }
    }

    private void setupComponents(FormMode formMode) {
        disableAllButtons();
        switch(formMode) {
            case FORM_ADD:
                btnSave.setEnabled(true);
                break;
            case FORM_VIEW:
                btnDelete.setEnabled(true);
                btnEnableChanges.setEnabled(true);
                
                //zabrani izmenu vrednosti
                txtId.setEnabled(false);
                txtName.setEnabled(false);
                txtDescription.setEnabled(false);
                break;
                
        }
    }
    
    private void disableAllButtons() {
        btnCancel.setEnabled(false);
                btnDelete.setEnabled(false);
                btnEdit.setEnabled(false);
                btnEnableChanges.setEnabled(false);
                btnSave.setEnabled(false);
    }
