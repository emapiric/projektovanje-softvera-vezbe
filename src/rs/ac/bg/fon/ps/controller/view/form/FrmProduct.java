    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Product product = new Product();
        product.setId(Long.parseLong(txtId.getText().trim()));
        product.setName(txtName.getText().trim());
        product.setDescription(txtDescription.getText().trim());
        product.setPrice(new BigDecimal(txtPrice.getText().trim()));
        product.setMeasurementUnit((MeasurementUnit) cbMeasurementUnit.getSelectedItem());
        product.setManufacturer((Manufacturer) cbManufacturer.getSelectedItem());
        Controller controller = Controller.getInstance();
        try {
            controller.addProduct(product);
            JOptionPane.showMessageDialog(this, "Product successfully saved");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

